public class OperatorsDemo {
    public static void main(String[] args) {
        int x = 10;

        int y;


        y = --x ;

        // post increment  - assign first and then increment
        //  X
        System.out.println( "X :"+x);
        System.out.println("  Y :"+y);


    }
}

